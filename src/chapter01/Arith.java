package chapter01;

public class Arith {

	public static void main(String[] args) {
		int a = 7;
		int b = 3;

		System.out.println(a+b);
		System.out.println(a-b);
		System.out.println(a*b);
		System.out.println(a/b);
		System.out.println(a%b);
		System.out.println(-a);
		System.out.println(++a);
		System.out.println(--b); 	
		System.out.println(a++);
		System.out.println(b--);
		System.out.println(10/3.);

	}

}
